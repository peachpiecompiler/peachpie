<font size="+1"><b>Extended Product Information:</b></font><br>
As you would expect from Team Durango, precision, quality and performance are the hallmarks of the DNX408. 1/8 scale nitro buggy racing makes the toughest demands of any RC car racing class,
so Team Durango have designed a car that is as strong as it is fast!
<p>
The 5mm thick hard anodized chassis is one of the narrowest, if not the narrowest on the market. Not only is the chassis hard anodized, it has been machined to minimize weight and give the
DNX408 class-leading center of gravity, with almost all the hardware sunk down into it.
<p>
Hard anodized alloy bulkheads front and rear keep the weight low and the car strong. Gearbox cases front and rear are molded and are separate from the load-bearing bulkheads. This gives
minimal flex, the best bearing alignment, dirt shielding and super-easy removal of whole differential and CVD, just like the DEX410.
<p>
PTFE coated ultra-smooth 15mm big-bore threaded alloy shocks feature alloy caps, an alloy cartridge bottom and polished shock shafts. Mounted on 5mm hard anodized shock towers, the shock
absorbers on the DNX408 are as smooth as possible giving you the best \'feel\' of your car as it soaks-up the roughest of tracks.
<p>
The single-piece CNC machined engine mount with gold anodized memory plate is designed for maximum engine performance and easy maintenance with a marked-up chassis for perfect-every-time
engine alignment.
<p>
In-board and out-board CVD joints front and rear, with rubber CVD boot protection, minimize debris problems in the race and maintenance between meetings.
<p>
The fuel tank inlet complete with dirt guard - is center-mounted for easy pit-lane access.
<p>
The DEX408 has the lowest bodyshell in its class as this has many benefits, like lowering the center of gravity even more; and the minimal frontal area punches through the air allowing more
air flow over the rear wing; it also helps with heat management.
<p>
The engine head in a DNX408 gets the most direct clean airflow of any car. The bodyshell is designed to direct as much air as possible over the bodyshell at the engine head, rather than
through the body. The central engine and low bodyshell mean that all shock bodies are also in clean airflow.
<p>
The DNX408\'s body is long and slim at the rear which encloses the exhaust header and insulates the left rear shock, while it is trimmed to help direct/suck hot air away from either rear
shock.
<p>
The super-low body ensures maximum flow over the adjustable rear wing, while the wing sits low to again lower the CG. The over-sized end-plates (ROAR legal) not only ensure all-round
stability, they help to protect the engine head in crashes.
<p>
The forward-mounted engine (which is 20mm further forward than some of our competitors\' race buggies) gives ideal weight distribution and room for everything to be mounted as low as possible
in the super-strong yet incredibly lightweight chassis.
<p>
The steering is set at the same up-angle as the kickup to remove bump steer, while the Ackerman link moves under the front CVD.
<p>
The rear wing is as close to the rear wheels as possible, thanks to the forward shock position. This drives the down-force directly to the rear wheels. Too many wings on other 1/8 buggies are
way out back which means more wheelies than down-force.
<p>
